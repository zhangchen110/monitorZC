package com.flyusoft.components.amcharts;

/** 
 * 饼状图数据对象
 */
public class PieData {
	//要显示的内容
	private String title;
	//是否显示切出去的特效 true-是,非true为否
	private String pullOut;
	//要显示的数值
	private String value;

	/**
	 * 示例：要显示2008年奥运会金牌数对比
	 * 中国是55，美国40，德国30，俄罗斯35，其他国家25
	 * 数据是：title是中国，value是55，true是显示切出去的特效；title是美国，value是40......
	 */

	public PieData(String title, String pullOut, String value) {
		this.title = title;
		this.pullOut = pullOut;
		this.value = value;
	}

	public String getTitle() {
		return title;
	}

	public void setTitle(String title) {
		this.title = title;
	}

	public String getPullOut() {
		return pullOut;
	}

	public void setPullOut(String pullOut) {
		this.pullOut = pullOut;
	}

	public String getValue() {
		return value;
	}

	public void setValue(String value) {
		this.value = value;
	}

}
