package com.flyusoft.apps.jointoil.entity;

import java.util.Date;

import javax.persistence.Entity;
import javax.persistence.Table;

import com.flyusoft.common.entity.IdEntity;

@Entity
@Table(name = "FLY_MACHINE_ROOM_MONITOR_DATA")
public class MachineRoomMonitorData extends IdEntity{
	private Double convergePipeTemperature;
	private Double convergePipePressure;
	private Double convergePipeCoolingWaterTemperature;
	private Double convergePipeCoolingWaterPressure;
	private Date time;
	private String roomNo;
	public Double getConvergePipeTemperature() {
		return convergePipeTemperature;
	}
	public void setConvergePipeTemperature(Double convergePipeTemperature) {
		this.convergePipeTemperature = convergePipeTemperature;
	}
	public Double getConvergePipePressure() {
		return convergePipePressure;
	}
	public void setConvergePipePressure(Double convergePipePressure) {
		this.convergePipePressure = convergePipePressure;
	}
	
	public Double getConvergePipeCoolingWaterTemperature() {
		return convergePipeCoolingWaterTemperature;
	}
	public void setConvergePipeCoolingWaterTemperature(
			Double convergePipeCoolingWaterTemperature) {
		this.convergePipeCoolingWaterTemperature = convergePipeCoolingWaterTemperature;
	}
	public Double getConvergePipeCoolingWaterPressure() {
		return convergePipeCoolingWaterPressure;
	}
	public void setConvergePipeCoolingWaterPressure(
			Double convergePipeCoolingWaterPressure) {
		this.convergePipeCoolingWaterPressure = convergePipeCoolingWaterPressure;
	}
	public Date getTime() {
		return time;
	}
	public void setTime(Date time) {
		this.time = time;
	}
	public String getRoomNo() {
		return roomNo;
	}
	public void setRoomNo(String roomNo) {
		this.roomNo = roomNo;
	}
	
}
